public class EndWithMethodDemo {

    /*

    Java String endsWith()

    The java string endsWith() method checks if this string ends with given suffix.
    It returns true if this string ends with given suffix else returns false.

     */

    public static void main(String[] args) {

        String s1 = "I wanna Save Sasha Johan from nightMake on Elm street";

        System.out.println(s1.endsWith("d"));

        System.out.println(s1.endsWith("bad"));

    }
}
